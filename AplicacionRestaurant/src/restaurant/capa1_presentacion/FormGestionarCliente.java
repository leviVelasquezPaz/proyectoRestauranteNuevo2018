    private void tablaClientesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMousePressed
        int fila = tablaClientes.getSelectedRow();
        String codigo = tablaClientes.getValueAt(fila, 0).toString();

        try {
            clienteSeleccionado = gestionarClienteServicio.buscarClientePorCodigo(Integer.parseInt(codigo.trim()));
            if (TIPO_USUARIO == TIPO_MOZO) {
                clienteSeleccionado = clienteSeleccionado;
                this.dispose();
            } else {
                inabilitarBotones(false);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tablaClientesMousePressed

