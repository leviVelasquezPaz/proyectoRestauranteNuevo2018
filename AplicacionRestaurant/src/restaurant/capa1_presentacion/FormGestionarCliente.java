    private void inicializarTabla() {
        Tabla tabla = new Tabla();
        tabla.agregarColumna(new Columna("Codigo", "java.lang.Integer"));
        tabla.agregarColumna(new Columna("Nombre", "java.lang.String"));
        tabla.agregarColumna(new Columna("Paterno", "java.lang.String"));
        tabla.agregarColumna(new Columna("Materno", "java.lang.String"));
        tabla.agregarColumna(new Columna("DNI", "java.lang.String"));
        tabla.agregarColumna(new Columna("Estado", "java.lang.String"));
        ModeloTabla modeloTabla = new ModeloTabla(tabla);
        tablaClientes.setModel(modeloTabla);
    }

    private void obtenerDatosTabla() {
        Fila filaTabla;
        try {
            ModeloTabla modeloTabla = (ModeloTabla) tablaClientes.getModel();
            modeloTabla.eliminarTotalFilas();
            if (listaCliente.size() == 0) {
                JOptionPane.showMessageDialog(null, "No hay productos registrados ");
            } else {
                for (Cliente cliente : listaCliente) {
                    filaTabla = new Fila();
                    filaTabla.agregarValorCelda(cliente.getClienteCodigo());
                    filaTabla.agregarValorCelda(cliente.getClienteNombre());
                    filaTabla.agregarValorCelda(cliente.getClientePaterno());
                    filaTabla.agregarValorCelda(cliente.getClienteMaterno());
                    filaTabla.agregarValorCelda(cliente.getClienteDni());
                    filaTabla.agregarValorCelda(cliente.getClienteEstado());
                    modeloTabla.agregarFila(filaTabla);
                }
                modeloTabla.refrescarDatos();
            }

        } catch (Exception e) {
            Mensaje.mostrarErrorSistema(this);
        }

    }

